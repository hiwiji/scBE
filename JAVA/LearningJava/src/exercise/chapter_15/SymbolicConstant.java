public class SymbolicConstant {

    public static void main(String[] args) {
        // 상수식 이름 짓기
        final int MY_INT = 5;
        //MY_INT = 10;

        final double MY_DOUBLE;
        MY_DOUBLE = 5.0;

        // final은 한번 정의하면 더이상 안됨
        //MY_DOUBLE = 10.0;
    }
}
